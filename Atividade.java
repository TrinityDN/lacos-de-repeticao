
public class Atividade {
	
	public static void main(String [] args) {
		
		//somente numeros impares de 1 a 100
		
		for(int x = 1; x <= 100; x = x + 2) {
			
			System.out.println(x);
			
		}
		
		//somente numeros pares de 1 a 100
		
		
		System.out.println("--------------");
		
		
		for(int x = 0; x <= 100; x = x + 2) {
			
			System.out.println(x);
		}
	}

}
