
public class TesteRepeticao {
	
	//comentário de uma linha
	//método principal mais 
	
	public static void main(String [] args) {
		
		//primeiro laco de repetição
		//impressão dos números de 1 a 10
		//for
		
		for(int x = 10; x > 0; x--) {
			
			//int x = 1 -> atribuição
			// x < 11 -> condição, ou seja, ficara no for até 10
			//x ++ -> incremento, ou seja, adiciona uma unidade de cada vez
			// x++ -> x = x + 1
			// x-- -> decremento
			// x-- -> x  = x - 1
			// x = x + 2 para incrementar a multiplicação, x vai de 2 em 2, duas unidades ao x
			//imprimindo o valor de x
			
			System.out.println(x);
			
		}
		
			
			for(int x = 1; x <= 100; x++) {
				
			
				System.out.println(x);
				
			}
			
			System.out.println("--------------");
			
			for(int x = 100; x > 1; x--) {
				
				System.out.println(x);
				
			}
				
	}

}

